import express from "express";
import dotenv  from "dotenv";
import helmet  from "helmet";
import cors    from "cors";
import rateLimit from "express-rate-limit";
import cookieParser from "cookie-parser";
import morgan  from "morgan";

import { connectDB }       from "./config/db.js";
import { errorHandler }    from "./middleware/errorHandler.js";

import uploadRoutes from "./routes/upload.routes.js";
import authRoutes   from "./routes/auth.routes.js";
import userRoutes   from "./routes/user.routes.js";
import adminRoutes  from "./routes/admin.routes.js";

dotenv.config();
await connectDB();

const app = express();

/* â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
   Global middle-ware
   â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€ */
app.use(helmet());
app.use(cors({ origin: true, credentials: true }));
app.use(morgan("dev"));
app.use(express.json());
app.use(cookieParser());

/* â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
   Rate-limit: 100 req / minute
   BUT weâ€™ll exempt uploads.
   â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€ */
const generalLimiter = rateLimit({
  windowMs: 60_000,
  max: 100,
});

/* â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
   ROUTES
   â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€ */

/* 1ï¸âƒ£  Uploads first â†’ NO limiter */
app.use("/api/uploads", uploadRoutes);

/* 2ï¸âƒ£  Apply limiter to every other /api route */
app.use("/api", generalLimiter);

/* 3ï¸âƒ£  All remaining routers inherit the limiter   */
app.use("/api/auth",  authRoutes);
app.use("/api/users", userRoutes);
app.use("/api/admin", adminRoutes);

/* â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
   Block any direct hit to /uploads/*
   (legacy static path is now closed)
   â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€ */
app.all(/^\/uploads\/.*$/, (_req, res) => res.sendStatus(404));

/* â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
   Fallback + error handler
   â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€ */
app.use((_, res) => res.sendStatus(404));
app.use(errorHandler);

/* â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
   Start server
   â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€ */
app.listen(process.env.PORT, () =>
  console.log(`ðŸš€ Server running on port ${process.env.PORT}`)
);